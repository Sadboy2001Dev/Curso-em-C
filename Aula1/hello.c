#include <stdio.h>

int main(void)
{
    printf("Hello World\n");
    return (0);
};

/*
    int main() / int main(void) -> funcao principal
    Onde roda todo o programa

    printf -> comando de saida/ output

    #include <stdio.h> : biblioteca de entrada e saida de dados (INPUT & OUTPUT)
    todas as bibliotecas a serem incluidas devem ser antecipadas pela frase * #include <nome-da-biblioteca>*
    o nome-da-biblioteca deve estar entre "<" & ">"

    O programa feito é um programa que imprimime "Hello World" na tecla/ecrã
*/