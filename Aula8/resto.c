#include <stdio.h>

int main()
{
    // % (modulo)-> percentagem
    // usado para calcuklar o resto da divisao
    printf("Resto da divisao entre 3 e 2 = %i\n", 3 % 2);
    printf("Resto da divisao entre 4 e 2 = %d\n", 4 % 2);
    printf("Resto da divisao entre 5 e 2 = %d\n", 5 % 2);
    printf("Resto da divisao entre 6 e 2 = %d\n", 6 % 2);
    printf("Resto da divisao entre 11 e 3 = %d", 11 % 3);
    return (0);
}